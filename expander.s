#include <avr/io.h>
 
#define rxstate r2

; USART0_RCX
.global __vector_17
__vector_17:
SBRC rxstate, 1
RJMP RX23
SBRS rxstate, 0
JMP RX0
JMP RX1
RX23:
SBRS rxstate, 0
JMP RX2
JMP RX3

RX0:
RETI
RX1:
RETI
RX2:
RETI
RX3:
RETI

.global main
main:
	ldi r16,0b00100000
	out VPORTB_DIR,r16
	out VPORTB_OUT,r16
	rjmp main
.end

